"""Analysis — recovery fraction + per-class breakdown (issue #514).

Consumes result JSONLs (contract #500) and emits one stats JSON plus a
markdown summary. Every number destined for the paper is computed by ONE
named function here and regenerated by one command:

    python eval/loop_eval/analyze.py --results eval/loop_eval/results/b1
    python eval/loop_eval/analyze.py --synthetic 400        # day-one self-check

Frozen-contract definitions used (SCHEMA.md §4):
- success denominator = outcome_class ∈ {completed, honest_giveup}; every
  other outcome is EXCLUDED and reported separately — never silently.
- recovery fraction per class = recovery ÷ (recoverable tasks whose
  iteration 1 failed), grouped by failure_class.
- headline gap = selection_correct_first AND NOT success at steps=1.
- never-retriggered residual = NOT success AND NOT loop_triggered at steps>1.
- thrash on unrecoverable = iterations_used > 1 where recoverable is false.
- cost per recovery = Σ loop_overhead_tokens over recovered ÷ recoveries.

Honesty rules: n is printed next to every rate; paired deltas get seeded
bootstrap CIs (percentile, 10k resamples); fewer than 5 pairs → the CI is
withheld and the summary says so. No significance theater.
"""
from __future__ import annotations

import argparse
import json
import random
import sys
from collections import defaultdict
from pathlib import Path
from statistics import mean

HERE = Path(__file__).resolve().parent
BOOTSTRAP_N = 10_000
BOOTSTRAP_SEED = 514
MIN_PAIRS_FOR_CI = 5

sys.path.insert(0, str(HERE))
import schema  # noqa: E402


# ── loading ──────────────────────────────────────────────────────────────────

def load_records(results_dir: Path) -> tuple[list[dict], dict]:
    """All parseable records + a load report (invalid/torn lines counted)."""
    records, report = [], {"files": 0, "invalid_schema": 0, "torn_lines": 0}
    for path in sorted(results_dir.glob("*.jsonl")):
        report["files"] += 1
        for line in path.read_text(encoding="utf-8").splitlines():
            if not line.strip():
                continue
            try:
                rec = json.loads(line)
            except json.JSONDecodeError:
                report["torn_lines"] += 1
                continue
            if schema.validate_result(rec):
                report["invalid_schema"] += 1
                continue
            records.append(rec)
    return records, report


def cond_key(rec: dict) -> str:
    c = rec["condition"]
    return f"{c['model']}|steps{c['tool_loop_max_steps']}"


def in_denominator(rec: dict) -> bool:
    return rec["outcome_class"] in schema.DENOMINATOR_OUTCOMES


def _rate(hits: int, n: int) -> float | None:
    return round(hits / n, 4) if n else None


# ── the paper's numbers, one function each ───────────────────────────────────

def success_rate_per_condition(records: list[dict]) -> dict:
    out: dict = {}
    for key in sorted({cond_key(r) for r in records}):
        denom = [r for r in records if cond_key(r) == key and in_denominator(r)]
        excluded = defaultdict(int)
        for r in records:
            if cond_key(r) == key and not in_denominator(r):
                excluded[r["outcome_class"]] += 1
        out[key] = {
            "n": len(denom),
            "success_rate": _rate(sum(r["success"] for r in denom), len(denom)),
            "selection_accuracy": _rate(
                sum(r["selection_correct_first"] for r in denom), len(denom)),
            "excluded": dict(excluded),
        }
    return out


def success_by_class_per_condition(records: list[dict]) -> dict:
    """Fig 1's data: success rate per (condition, failure_class)."""
    out: dict = {}
    for key in sorted({cond_key(r) for r in records}):
        denom = [r for r in records if cond_key(r) == key and in_denominator(r)]
        per_class: dict = {}
        for klass in sorted({r["failure_class"] for r in denom}):
            rs = [r for r in denom if r["failure_class"] == klass]
            per_class[klass] = {
                "n": len(rs),
                "success_rate": _rate(sum(r["success"] for r in rs), len(rs)),
            }
        out[key] = per_class
    return out


def paired_deltas(records: list[dict]) -> dict:
    """Per model: paired per-task success delta (steps>1 minus steps=1) with
    a seeded bootstrap CI. Tasks are paired across conditions — use it."""
    out: dict = {}
    by_model = defaultdict(list)
    for r in records:
        if in_denominator(r):
            by_model[r["condition"]["model"]].append(r)
    for model, recs in sorted(by_model.items()):
        base = {r["task_id"]: r["success"] for r in recs
                if r["condition"]["tool_loop_max_steps"] == 1}
        loop = {r["task_id"]: r["success"] for r in recs
                if r["condition"]["tool_loop_max_steps"] > 1}
        pairs = sorted(set(base) & set(loop))
        deltas = [int(loop[t]) - int(base[t]) for t in pairs]
        entry: dict = {"n_pairs": len(pairs),
                       "mean_delta": round(mean(deltas), 4) if deltas else None}
        if len(pairs) >= MIN_PAIRS_FOR_CI:
            rng = random.Random(BOOTSTRAP_SEED)
            resamples = []
            for _ in range(BOOTSTRAP_N):
                sample = [deltas[rng.randrange(len(deltas))]
                          for _ in range(len(deltas))]
                resamples.append(mean(sample))
            resamples.sort()
            entry["ci95"] = [round(resamples[int(0.025 * BOOTSTRAP_N)], 4),
                             round(resamples[int(0.975 * BOOTSTRAP_N)], 4)]
        else:
            entry["ci95"] = None
            entry["ci_note"] = (f"only {len(pairs)} pairs (<{MIN_PAIRS_FOR_CI})"
                                " — CI withheld")
        out[model] = entry
    return out


def recovery_by_class(records: list[dict]) -> dict:
    """SCHEMA §4: recovery ÷ (recoverable tasks whose iteration 1 failed)."""
    out: dict = {}
    eligible = [r for r in records if in_denominator(r)
                and r["recoverable"] is True
                and not r["iterations"][0]["result"]["success"]]
    for klass in sorted({r["failure_class"] for r in eligible}):
        rs = [r for r in eligible if r["failure_class"] == klass]
        out[klass] = {"n_eligible": len(rs),
                      "recovery_fraction": _rate(
                          sum(r["recovery"] for r in rs), len(rs))}
    return out


def thrash_on_unrecoverable(records: list[dict]) -> dict:
    rs = [r for r in records if in_denominator(r)
          and r["recoverable"] is False]
    thrash = [r for r in rs if r["iterations_used"] > 1]
    return {"n": len(rs), "thrash_rate": _rate(len(thrash), len(rs)),
            "honest_giveup_rate": _rate(
                sum(r["outcome_class"] == "honest_giveup" for r in rs),
                len(rs))}


def headline_gap(records: list[dict]) -> dict:
    """selection_correct_first AND NOT success at steps=1 — the
    necessary-but-not-sufficient decomposition."""
    rs = [r for r in records if in_denominator(r)
          and r["condition"]["tool_loop_max_steps"] == 1]
    gap = [r for r in rs if r["selection_correct_first"] and not r["success"]]
    sel = [r for r in rs if r["selection_correct_first"]]
    return {"n_steps1": len(rs),
            "selection_accuracy": _rate(len(sel), len(rs)),
            "selected_right_but_failed_rate": _rate(len(gap), len(rs))}


def never_retriggered_residual(records: list[dict]) -> dict:
    rs = [r for r in records if in_denominator(r)
          and r["condition"]["tool_loop_max_steps"] > 1]
    resid = [r for r in rs if not r["success"] and not r["loop_triggered"]]
    return {"n_steps_gt1": len(rs),
            "never_retriggered_rate": _rate(len(resid), len(rs))}


def cost_stats(records: list[dict]) -> dict:
    out: dict = {}
    for key in sorted({cond_key(r) for r in records}):
        rs = [r for r in records if cond_key(r) == key and in_denominator(r)]
        recovered = [r for r in rs if r["recovery"]]
        overhead = sum(r["cost"]["loop_overhead_tokens"] for r in recovered)
        out[key] = {
            "n": len(rs),
            "mean_wall_ms": round(mean(r["cost"]["wall_ms"] for r in rs), 1)
            if rs else None,
            "mean_tokens_total": round(mean(
                r["cost"]["tokens_in"] + r["cost"]["tokens_out"]
                for r in rs), 1) if rs else None,
            "recoveries": len(recovered),
            "cost_per_recovery_tokens": (round(overhead / len(recovered), 1)
                                         if recovered else None),
        }
    return out


def completeness(records: list[dict], expected_ids: list[str] | None) -> dict:
    """Partial batches are analyzable but must SAY what's missing."""
    out: dict = {}
    for key in sorted({cond_key(r) for r in records}):
        have = {r["task_id"] for r in records if cond_key(r) == key}
        entry: dict = {"tasks_recorded": len(have)}
        if expected_ids is not None:
            missing = sorted(set(expected_ids) - have)
            entry["tasks_expected"] = len(expected_ids)
            entry["missing"] = missing
            entry["complete"] = not missing
        else:
            entry["note"] = "no manifest — expected task set unknown"
        out[key] = entry
    return out


# ── orchestration ────────────────────────────────────────────────────────────

def analyze(records: list[dict], load_report: dict,
            expected_ids: list[str] | None) -> dict:
    return {
        "n_records": len(records),
        "load_report": load_report,
        "completeness": completeness(records, expected_ids),
        "success_rate_per_condition": success_rate_per_condition(records),
        "success_by_class_per_condition": success_by_class_per_condition(records),
        "paired_deltas_by_model": paired_deltas(records),
        "recovery_by_failure_class": recovery_by_class(records),
        "thrash_on_unrecoverable": thrash_on_unrecoverable(records),
        "headline_gap": headline_gap(records),
        "never_retriggered_residual": never_retriggered_residual(records),
        "cost": cost_stats(records),
    }


def to_markdown(stats: dict) -> str:
    lines = ["# loop_eval analysis (issue #514)", ""]
    lr = stats["load_report"]
    lines.append(f"{stats['n_records']} valid records from {lr['files']} "
                 f"file(s); {lr['torn_lines']} torn line(s), "
                 f"{lr['invalid_schema']} schema-invalid record(s) skipped.")
    lines.append("")

    lines.append("## Completeness (missing data is named, not hidden)")
    for key, c in stats["completeness"].items():
        if c.get("complete") is True:
            lines.append(f"- `{key}`: complete ({c['tasks_recorded']} tasks)")
        elif "missing" in c:
            lines.append(f"- `{key}`: **INCOMPLETE** — "
                         f"{c['tasks_recorded']}/{c['tasks_expected']}, "
                         f"missing: {', '.join(c['missing'][:8])}"
                         + (" …" if len(c["missing"]) > 8 else ""))
        else:
            lines.append(f"- `{key}`: {c['tasks_recorded']} tasks — {c['note']}")
    lines.append("")

    lines.append("## Success rate per condition")
    lines.append("| condition | n | success | excluded |")
    lines.append("|---|---|---|---|")
    for key, s in stats["success_rate_per_condition"].items():
        lines.append(f"| `{key}` | {s['n']} | {s['success_rate']} | "
                     f"{json.dumps(s['excluded'])} |")
    lines.append("")

    lines.append("## Paired per-task delta (steps>1 − steps=1)")
    for model, d in stats["paired_deltas_by_model"].items():
        ci = (f"95% CI {d['ci95']}" if d.get("ci95")
              else d.get("ci_note", "CI unavailable"))
        lines.append(f"- `{model}`: Δ={d['mean_delta']} over "
                     f"{d['n_pairs']} pairs ({ci})")
    lines.append("")

    lines.append("## Recovery fraction by failure class")
    lines.append("| class | eligible (iter-1 failed, recoverable) | recovery |")
    lines.append("|---|---|---|")
    for klass, r in stats["recovery_by_failure_class"].items():
        lines.append(f"| {klass} | {r['n_eligible']} | "
                     f"{r['recovery_fraction']} |")
    lines.append("")

    t = stats["thrash_on_unrecoverable"]
    lines.append(f"**Unrecoverable class** (n={t['n']}): thrash rate "
                 f"{t['thrash_rate']}, honest-giveup rate "
                 f"{t['honest_giveup_rate']}.")
    g = stats["headline_gap"]
    lines.append(f"**Headline gap** (steps=1, n={g['n_steps1']}): selection "
                 f"accuracy {g['selection_accuracy']}, selected-right-but-"
                 f"failed {g['selected_right_but_failed_rate']}.")
    nr = stats["never_retriggered_residual"]
    lines.append(f"**Never-retriggered residual** (steps>1, "
                 f"n={nr['n_steps_gt1']}): {nr['never_retriggered_rate']}.")
    lines.append("")

    lines.append("## Cost")
    lines.append("| condition | n | mean wall ms | mean tokens | recoveries "
                 "| tokens / recovery |")
    lines.append("|---|---|---|---|---|---|")
    for key, c in stats["cost"].items():
        lines.append(f"| `{key}` | {c['n']} | {c['mean_wall_ms']} | "
                     f"{c['mean_tokens_total']} | {c['recoveries']} | "
                     f"{c['cost_per_recovery_tokens']} |")
    lines.append("")
    return "\n".join(lines)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--results", help="directory of result *.jsonl files")
    parser.add_argument("--manifest", help="batch manifest json (for the "
                                           "expected task set)")
    parser.add_argument("--synthetic", type=int, default=0,
                        help="self-check on N synthetic records (no files)")
    parser.add_argument("--out", help="stats JSON path")
    parser.add_argument("--md", help="markdown summary path")
    args = parser.parse_args(argv)

    expected_ids = None
    if args.synthetic:
        records = schema.synthetic_results(args.synthetic)
        load_report = {"files": 0, "invalid_schema": 0, "torn_lines": 0,
                       "synthetic": args.synthetic}
    elif args.results:
        records, load_report = load_records(Path(args.results))
        if args.manifest:
            expected_ids = json.loads(
                Path(args.manifest).read_text(encoding="utf-8"))["task_ids"]
    else:
        parser.error("pass --results DIR or --synthetic N")

    if not records:
        print("no valid records to analyze", file=sys.stderr)
        return 1

    stats = analyze(records, load_report, expected_ids)
    md = to_markdown(stats)
    if args.out:
        Path(args.out).write_text(json.dumps(stats, indent=2,
                                             ensure_ascii=False),
                                  encoding="utf-8")
    if args.md:
        Path(args.md).write_text(md, encoding="utf-8")
    print(md)
    return 0


if __name__ == "__main__":
    sys.exit(main())
